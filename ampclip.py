def setup_logging(output_dir: Path, log_level: int = logging.INFO) -> logging.Logger:
    """Dual-sink logger: DEBUG to file, INFO (or as configured) to console."""
    log_dir = output_dir / "logs"
    log_dir.mkdir(parents=True, exist_ok=True)

    fmt       = "[%(asctime)s] [%(levelname)-8s] %(message)s"
    datefmt   = "%Y-%m-%d %H:%M:%S"
    formatter = logging.Formatter(fmt, datefmt=datefmt)

    fh = logging.FileHandler(log_dir / "ampclip.log", encoding="utf-8")
    fh.setFormatter(formatter)
    fh.setLevel(logging.DEBUG)

    ch = logging.StreamHandler(sys.stdout)
    ch.setFormatter(formatter)
    ch.setLevel(log_level)

    logger = logging.getLogger("AmpClip")
    logger.setLevel(logging.DEBUG)
    logger.addHandler(fh)
    logger.addHandler(ch)
    logger.propagate = False
    return logger


