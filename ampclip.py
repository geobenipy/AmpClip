def run(
    input_path:           str,
    threshold:            Optional[float] = None,
    output_path:          Optional[str]   = None,
    taper_samples:        int             = 0,
    invert:               bool            = False,
    verify:               bool            = True,
    make_plots:           bool            = True,
    plot_n_traces:        int             = 300,
    threshold_mode:       str             = "absolute",
    threshold_percentile: float           = 99.9,
    threshold_rms_factor: float           = 10.0,
    scan_traces:          int             = 5000,
    log_level:            int             = logging.INFO,
) -> str:
    """
    End-to-end processing pipeline.  All parameters mirror config.py.

    Returns
    -------
    str  —  absolute path to the output SEG-Y file.
    """
    out_dir = create_output_dir(input_path)
    logger  = setup_logging(out_dir, log_level=log_level)

    logger.info("=" * 66)
    logger.info(f"AMPCLIP v{__version__}  —  Amplitude Threshold Muting Tool")
    logger.info(f"Author: {__author__}")
    logger.info("=" * 66)
    logger.info(f"Input file : {Path(input_path).resolve()}")
    logger.info(f"Output dir : {out_dir.resolve()}")

    if not Path(input_path).exists():
        logger.error(f"Input file not found: {input_path}")
        raise FileNotFoundError(f"SEG-Y not found: {input_path}")

    logger.info(f"Input size : {Path(input_path).stat().st_size / 1e6:.2f} MB")

    # Determine threshold
    if threshold is None:
        if threshold_mode == "percentile":
            threshold = estimate_threshold_from_percentile(
                input_path, threshold_percentile, scan_traces, logger)
        elif threshold_mode == "rms":
            threshold = estimate_threshold_from_rms_factor(
                input_path, threshold_rms_factor, scan_traces, logger)
        else:
            raise ValueError(
                "THRESHOLD is None but THRESHOLD_MODE is 'absolute'. "
                "Either set THRESHOLD to a number, or change THRESHOLD_MODE "
                "to 'percentile' or 'rms' in config.py."
            )

    if threshold <= 0:
        raise ValueError(f"Threshold must be > 0, got {threshold}.")

    logger.info(f"Threshold  : {threshold:.6g}")

    # Determine output path
    if output_path is None:
        stem        = Path(input_path).stem
        suffix      = Path(input_path).suffix or ".sgy"
        output_path = str(out_dir / f"{stem}_ampclip{suffix}")

    logger.info(f"Output     : {Path(output_path).resolve()}")

    # Process
    stats = clip_segy(
        input_path    = input_path,
        output_path   = output_path,
        threshold     = threshold,
        taper_samples = taper_samples,
        invert        = invert,
        logger        = logger,
    )

    logger.info("Processing complete.")
    logger.info(f"  Affected traces : {stats.n_traces_affected:,} / {stats.n_traces_total:,}")
    logger.info(f"  Muted samples   : {stats.n_samples_muted:,} / {stats.n_samples_total:,}")
    logger.info(f"  Peak in / out   : {stats.max_amplitude_in:.4g} / "
                f"{stats.max_amplitude_out:.4g}")

    # Verify
    verified = False
    if verify:
        verified = verify_output(input_path, output_path, threshold, logger=logger)

    # Plots
    if make_plots:
        make_qc_plots(
            input_path  = input_path,
            output_path = output_path,
            threshold   = threshold,
            stats       = stats,
            plot_dir    = out_dir / "plots",
            n_traces    = plot_n_traces,
            logger      = logger,
        )

    # Report
    config_dict = {
        "threshold":              threshold,
        "threshold_mode":         threshold_mode,
        "taper_samples":          taper_samples,
        "invert":                 invert,
        "make_plots":             make_plots,
        "verify":                 verify,
        "version":                __version__,
    }
    write_report(stats, input_path, output_path, out_dir,
                 config_dict, verified, logger)

    return str(Path(output_path).resolve())


