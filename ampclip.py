def write_report(
    stats:       AmpClipStats,
    input_path:  str,
    output_path: str,
    output_dir:  Path,
    config:      Dict[str, Any],
    verified:    bool,
    logger:      Optional[logging.Logger] = None,
):
    """Write plain-text and JSON processing reports."""
    log = logger or logging.getLogger("AmpClip")
    ts  = datetime.datetime.now().strftime("%Y-%m-%d %H:%M:%S")

    pct_traces = stats.n_traces_affected / max(stats.n_traces_total, 1) * 100
    pct_samps  = stats.n_samples_muted   / max(stats.n_samples_total, 1) * 100
    rms_change = ((stats.global_rms_out - stats.global_rms_in)
                  / max(stats.global_rms_in, 1e-30) * 100)

    lines = [
        "=" * 72,
        "  AMPCLIP — Processing Report",
        f"  by Beni  |  {ts}",
        "=" * 72, "",
        "  FILES",
        "  " + "-" * 40,
        f"  Input          : {input_path}",
        f"  Output         : {output_path}",
        f"  Input size     : {Path(input_path).stat().st_size / 1e6:.2f} MB",
        f"  Output size    : {Path(output_path).stat().st_size / 1e6:.2f} MB",
        "",
        "  CONFIGURATION",
        "  " + "-" * 40,
    ]
    for k, v in config.items():
        lines.append(f"  {k:<28}: {v}")
    lines += [
        "",
        "  PROCESSING STATISTICS",
        "  " + "-" * 40,
        f"  Total traces         : {stats.n_traces_total:>12,}",
        f"  Affected traces      : {stats.n_traces_affected:>12,}   ({pct_traces:6.2f} %)",
        f"  Total samples        : {stats.n_samples_total:>12,}",
        f"  Muted samples        : {stats.n_samples_muted:>12,}   ({pct_samps:6.4f} %)",
        f"  Peak amplitude  [in] : {stats.max_amplitude_in:>18.6g}",
        f"  Peak amplitude [out] : {stats.max_amplitude_out:>18.6g}",
        f"  Global RMS      [in] : {stats.global_rms_in:>18.6g}",
        f"  Global RMS     [out] : {stats.global_rms_out:>18.6g}",
        f"  RMS change           : {rms_change:>+17.2f} %",
        "",
        "  VERIFICATION",
        "  " + "-" * 40,
        f"  Result: {'PASSED' if verified else 'FAILED  <-- check log'}",
        "",
        "=" * 72,
        "  AmpClip  —  by Beni",
        "=" * 72,
    ]

    report_dir = output_dir / "reports"
    report_dir.mkdir(parents=True, exist_ok=True)

    (report_dir / "ampclip_report.txt").write_text(
        "\n".join(lines), encoding="utf-8"
    )
    (report_dir / "ampclip_summary.json").write_text(
        json.dumps({
            "timestamp":            ts,
            "input_path":           str(input_path),
            "output_path":          str(output_path),
            "config":               config,
            "n_traces_total":       stats.n_traces_total,
            "n_traces_affected":    stats.n_traces_affected,
            "pct_traces_affected":  round(pct_traces, 4),
            "n_samples_total":      stats.n_samples_total,
            "n_samples_muted":      stats.n_samples_muted,
            "pct_samples_muted":    round(pct_samps, 6),
            "max_amplitude_in":     stats.max_amplitude_in,
            "max_amplitude_out":    stats.max_amplitude_out,
            "global_rms_in":        stats.global_rms_in,
            "global_rms_out":       stats.global_rms_out,
            "rms_change_pct":       round(rms_change, 4),
            "verification_passed":  verified,
        }, indent=2),
        encoding="utf-8",
    )

    log.info(f"Reports written to: {report_dir.resolve()}")

    print()
    print("─" * 56)
    print("  AmpClip — Summary")
    print("─" * 56)
    print(f"  Threshold        : {stats.threshold:.6g}")
    print(f"  Traces affected  : {stats.n_traces_affected:,} / "
          f"{stats.n_traces_total:,} ({pct_traces:.2f} %)")
    print(f"  Samples muted    : {stats.n_samples_muted:,} ({pct_samps:.4f} %)")
    print(f"  RMS change       : {rms_change:+.2f} %")
    print(f"  Verification     : {'PASSED' if verified else 'FAILED'}")
    print(f"  Output           : {Path(output_path).resolve()}")
    print("─" * 56)
    print()


