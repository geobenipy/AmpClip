def estimate_threshold_from_percentile(
    input_path:      str,
    percentile:      float = 99.9,
    max_scan_traces: int   = 5000,
    logger:          Optional[logging.Logger] = None,
) -> float:
    """Return the amplitude value at `percentile` of |amplitude| over a trace subset."""
    log = logger or logging.getLogger("AmpClip")
    log.info(f"Estimating threshold: {percentile:.4f}th percentile "
             f"(scanning up to {max_scan_traces:,} traces)...")

    abs_values: List[np.ndarray] = []
    with segyio.open(str(input_path), ignore_geometry=True) as f:
        n    = f.tracecount
        step = max(1, n // max_scan_traces)
        for i in tqdm(range(0, n, step), desc="Scanning amplitudes",
                      unit="trace", ncols=80):
            abs_values.append(np.abs(f.trace[i]))

    threshold = float(np.percentile(np.concatenate(abs_values), percentile))
    log.info(f"  -> Threshold at {percentile:.4f}th percentile: {threshold:.6g}")
    return threshold


def estimate_threshold_from_rms_factor(
    input_path:      str,
    rms_factor:      float = 10.0,
    max_scan_traces: int   = 5000,
    logger:          Optional[logging.Logger] = None,
) -> float:
    """Return rms_factor * global_RMS computed over a trace subset."""
    log = logger or logging.getLogger("AmpClip")
    log.info(f"Estimating threshold: {rms_factor:.1f} x global RMS "
             f"(scanning up to {max_scan_traces:,} traces)...")

    sum_sq = 0.0
    n_samp = 0
    with segyio.open(str(input_path), ignore_geometry=True) as f:
        n    = f.tracecount
        step = max(1, n // max_scan_traces)
        for i in tqdm(range(0, n, step), desc="Scanning RMS",
                      unit="trace", ncols=80):
            tr      = f.trace[i].astype(np.float64)
            sum_sq += np.sum(tr ** 2)
            n_samp += len(tr)

    global_rms = float(np.sqrt(sum_sq / max(n_samp, 1)))
    threshold  = rms_factor * global_rms
    log.info(f"  Global RMS: {global_rms:.6g}")
    log.info(f"  -> Threshold ({rms_factor:.1f} x RMS): {threshold:.6g}")
    return threshold


# ─────────────────────────────────────────────────────────────────────────────
#  TAPER
# ─────────────────────────────────────────────────────────────────────────────

def _apply_taper(trace: np.ndarray, muted: np.ndarray, taper_samples: int) -> np.ndarray:
    """Apply a cosine taper on both sides of each contiguous muted cluster."""
    if taper_samples <= 0:
        return trace

    result = trace.copy()
    ns     = len(trace)
    taper  = 0.5 * (1.0 - np.cos(np.linspace(0, np.pi, taper_samples)))

    transitions = np.diff(muted.astype(int), prepend=0, append=0)
    starts = np.where(transitions ==  1)[0]
    ends   = np.where(transitions == -1)[0]

    for s, e in zip(starts, ends):
        for k in range(taper_samples):
            pre = s - taper_samples + k
            if 0 <= pre < ns:
                result[pre] *= taper[k]
            post = e + k
            if 0 <= post < ns:
                result[post] *= taper[taper_samples - 1 - k]

    return result


# ─────────────────────────────────────────────────────────────────────────────
#  STATISTICS ACCUMULATOR
# ─────────────────────────────────────────────────────────────────────────────

class AmpClipStats:
    """Accumulates per-run processing statistics."""

    def __init__(self):
        self.n_traces_total     = 0
        self.n_traces_affected  = 0
        self.n_samples_total    = 0
        self.n_samples_muted    = 0
        self.max_amplitude_in   = 0.0
        self.max_amplitude_out  = 0.0
        self.global_rms_in      = 0.0
        self.global_rms_out     = 0.0
        self.threshold          = 0.0
        self.taper_samples      = 0
        self._sum_sq_in         = 0.0
        self._sum_sq_out        = 0.0
        # per-trace muted sample count — used for QC plot
        self.muted_per_trace: List[int] = []

    def update(self, trace_in: np.ndarray, trace_out: np.ndarray,
               muted: np.ndarray):
        n     = len(trace_in)
        n_mut = int(muted.sum())
        self.n_samples_total += n
        self.n_samples_muted += n_mut
        self.muted_per_trace.append(n_mut)
        if n_mut > 0:
            self.n_traces_affected += 1
        pk_in = float(np.max(np.abs(trace_in)))
        if pk_in > self.max_amplitude_in:
            self.max_amplitude_in = pk_in
        pk_out = float(np.max(np.abs(trace_out)))
        if pk_out > self.max_amplitude_out:
            self.max_amplitude_out = pk_out
        self._sum_sq_in  += float(np.sum(trace_in.astype(np.float64)  ** 2))
        self._sum_sq_out += float(np.sum(trace_out.astype(np.float64) ** 2))

    def finalise(self):
        denom = max(self.n_samples_total, 1)
        self.global_rms_in  = float(np.sqrt(self._sum_sq_in  / denom))
        self.global_rms_out = float(np.sqrt(self._sum_sq_out / denom))


# ─────────────────────────────────────────────────────────────────────────────
#  CORE PROCESSING
# ─────────────────────────────────────────────────────────────────────────────

def clip_segy(
    input_path:    str,
    output_path:   str,
    threshold:     float,
    taper_samples: int  = 0,
    invert:        bool = False,
    logger:        Optional[logging.Logger] = None,
) -> AmpClipStats:
    """
    Core single-pass processing function.

    Parameters
    ----------
    input_path    : input SEG-Y path
    output_path   : output SEG-Y path
    threshold     : amplitude threshold; |x| > threshold -> zeroed (invert=False)
    taper_samples : cosine taper half-width in samples (0 = hard mute)
    invert        : if True, zero |x| < threshold instead
    logger        : logger instance

    Returns
    -------
    AmpClipStats
    """
    log   = logger or logging.getLogger("AmpClip")
    stats = AmpClipStats()
    stats.threshold     = threshold
    stats.taper_samples = taper_samples

    log.info("Opening input file...")

    with segyio.open(str(input_path), ignore_geometry=True) as src:

        n_traces = src.tracecount
        ns       = int(src.samples.size)
        dt_us    = float(segyio.dt(src))

        stats.n_traces_total = n_traces

        log.info(f"  Traces        : {n_traces:,}")
        log.info(f"  Samples/trace : {ns:,}")
        log.info(f"  Sample rate   : {dt_us:.1f} µs")
        log.info(f"  Threshold     : {threshold:.6g}  "
                 f"({'keep-large mode' if invert else 'mute-large mode'})")
        if taper_samples > 0:
            log.info(f"  Taper         : {taper_samples} samples each side "
                     f"({taper_samples * dt_us * 1e-3:.2f} ms)")
        else:
            log.info(f"  Taper         : none (hard zeroing)")

        log.info("Creating output SEG-Y (preserving full input structure)...")
        spec            = segyio.spec()
        spec.sorting    = None
        spec.format     = src.format
        spec.samples    = src.samples.copy()
        spec.tracecount = n_traces

        with segyio.create(str(output_path), spec) as dst:

            # Copy binary header
            dst.bin.update(tsort=src.bin[segyio.BinField.SortingCode])
            for k in src.bin.keys():
                try:
                    dst.bin.update(**{k: src.bin[k]})
                except Exception:
                    pass

            # Copy all text headers
            for i in range(len(src.text)):
                try:
                    dst.text[i] = src.text[i]
                except Exception:
                    pass

            # Process traces
            log.info("Processing traces...")
            for i in tqdm(range(n_traces), desc="Muting amplitudes",
                          unit="trace", ncols=90):

                trace_in  = src.trace[i].copy().astype(np.float32)
                abs_tr    = np.abs(trace_in)
                muted     = (abs_tr < threshold) if invert else (abs_tr > threshold)

                trace_out          = trace_in.copy()
                trace_out[muted]   = 0.0

                if taper_samples > 0 and muted.any():
                    trace_out = _apply_taper(trace_out, muted, taper_samples)

                stats.update(trace_in, trace_out, muted)

                dst.trace[i]  = trace_out
                dst.header[i] = src.header[i]

    stats.finalise()
    return stats


# ─────────────────────────────────────────────────────────────────────────────
#  VERIFICATION
# ─────────────────────────────────────────────────────────────────────────────

def verify_output(
    input_path:  str,
    output_path: str,
    threshold:   float,
    n_check:     int = 200,
    logger:      Optional[logging.Logger] = None,
) -> bool:
    """
    Spot-check output file:
      1. Trace and sample counts match input.
      2. No output sample exceeds the threshold (with IBM-float rounding margin).
      3. All trace header bytes are identical to input for sampled traces.
    """
    log = logger or logging.getLogger("AmpClip")
    log.info("Verifying output file...")
    ok = True

    # Build integer-only TraceField map once (avoids method-object TypeError)
    field_map: Dict[str, int] = {
        k: getattr(segyio.TraceField, k)
        for k in dir(segyio.TraceField)
        if not k.startswith("_")
        and isinstance(getattr(segyio.TraceField, k), int)
    }

    # IBM float32 -> IEEE float32 roundtrip can introduce up to ~0.03 % error.
    # We use a 0.1 % tolerance so genuine violations still surface.
    AMP_TOLERANCE = 1.001

    try:
        with segyio.open(str(input_path),  ignore_geometry=True) as src, \
             segyio.open(str(output_path), ignore_geometry=True) as dst:

            if src.tracecount != dst.tracecount:
                log.error(f"  [FAIL] Trace count mismatch: "
                          f"{src.tracecount} vs {dst.tracecount}")
                ok = False
            else:
                log.debug(f"  [OK]   Trace count: {src.tracecount:,}")

            if len(src.samples) != len(dst.samples):
                log.error(f"  [FAIL] Sample count mismatch: "
                          f"{len(src.samples)} vs {len(dst.samples)}")
                ok = False
            else:
                log.debug(f"  [OK]   Sample count: {len(src.samples):,}")

            n   = src.tracecount
            rng = np.random.default_rng(seed=42)
            idx = np.sort(rng.choice(n, size=min(n_check, n), replace=False))

            amp_violations    = 0
            header_mismatches = 0

            for i in tqdm(idx, desc="Verifying",
                          unit="trace", ncols=80, leave=False):

                # Amplitude check
                if np.any(np.abs(dst.trace[i]) > threshold * AMP_TOLERANCE):
                    amp_violations += 1

                # Header check — compare by integer byte position only
                h_in  = src.header[i]
                h_out = dst.header[i]
                for byte_pos in field_map.values():
                    try:
                        if h_in[byte_pos] != h_out[byte_pos]:
                            header_mismatches += 1
                            break
                    except Exception:
                        pass

            if amp_violations:
                log.error(f"  [FAIL] {amp_violations} traces contain "
                          f"amplitudes above threshold (tolerance {AMP_TOLERANCE:.3f}x).")
                ok = False
            else:
                log.info(f"  [OK]   Amplitude check passed on {len(idx)} sampled traces.")

            if header_mismatches:
                log.error(f"  [FAIL] {header_mismatches} traces have header mismatches.")
                ok = False
            else:
                log.info(f"  [OK]   Header check passed on {len(idx)} sampled traces.")

    except Exception as exc:
        log.error(f"  Verification raised an exception: {exc}")
        ok = False

    if ok:
        log.info("Verification PASSED.")
    else:
        log.warning("Verification FAILED — inspect the log for details.")

    return ok


